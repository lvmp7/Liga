package br.com.nextevolution.Liga.model;

public class LimitesCompeticao {
	private int total_confronto_pro;
	private int total_confronto_free;
	private int criacao_confronto_pro;
	
	public int getTotal_confronto_pro() {
		return total_confronto_pro;
	}
	public void setTotal_confronto_pro(int total_confronto_pro) {
		this.total_confronto_pro = total_confronto_pro;
	}
	public int getTotal_confronto_free() {
		return total_confronto_free;
	}
	public void setTotal_confronto_free(int total_confronto_free) {
		this.total_confronto_free = total_confronto_free;
	}
	public int getCriacao_confronto_pro() {
		return criacao_confronto_pro;
	}
	public void setCriacao_confronto_pro(int criacao_confronto_pro) {
		this.criacao_confronto_pro = criacao_confronto_pro;
	}
	public int getCriacao_confronto_free() {
		return criacao_confronto_free;
	}
	public void setCriacao_confronto_free(int criacao_confronto_free) {
		this.criacao_confronto_free = criacao_confronto_free;
	}
	private int criacao_confronto_free;
}
